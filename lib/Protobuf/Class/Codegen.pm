=encoding utf-8

=head1 NAME

Protobuf::Class::Codegen - AOT renderer for proto3 message classes

=head1 SYNOPSIS

    use Protobuf::Class::Codegen;

    my $pkg  = Protobuf::Class::Codegen::package_for('temporal.api.common.v1', 'T::Api');
    # 'T::Api::Common::V1'

    my $src  = Protobuf::Class::Codegen::render_file($schema, $file, 'T::Api');
    my $path = Protobuf::Class::Codegen::output_path($file, 'T::Api');
    # 'T/Api/Common/V1/Message.pm'

=head1 DESCRIPTION

The rendering engine behind C<bin/protobuf-gen-perl>. It maps
protobuf packages to Perl namespaces and emits one C<.pm> per C<.proto> file.

Each emitted module reconstructs its file's L<Protobuf::Schema> tree from static
Perl (so the generated code carries B<no> parser or descriptor-set dependency)
and installs every message class through L<Protobuf::Class::Generator> — the exact
same build path the runtime uses. That shared path is what keeps the AOT-emitted
classes and the runtime-generated classes from drifting: there is only one
accessor/codec contract.

Output is B<deterministic>: messages are emitted sorted by full name and the
schema serializer (L<Protobuf::Schema::Serializer>) renders fields in a stable
order, so regenerating an unchanged C<.proto> is byte-identical.

=head1 FUNCTIONS

=head2 package_for

    package_for($proto_package, $prefix)

Maps a dotted protobuf package to a Perl namespace. Every component is
PascalCased; a C<$prefix> (e.g. C<'T::Api'>) replaces that many leading
components.

=head2 perl_class_for

    perl_class_for($message_full_name, $proto_package, $prefix)

Maps a message's fully-qualified name to its generated Perl class name.

=head2 output_path

    output_path($file, $prefix)

The C<.pm> path, relative to the output directory, for a L<Protobuf::Schema::File>.

=head2 render_file

    render_file($schema, $file, $prefix)

Returns the C<.pm> source text for a file.

